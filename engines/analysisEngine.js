function generateBarsRadar(stk,days=60){
  const rng=seedRng(parseInt(stk.sym,10)*997+13);
  const bars=[];
  let p=stk.p*(1-stk.pct/100*(days/20));
  const av=stk.avgVol||stk.v||1e6;
  for(let i=0;i<days;i++){
    const dr=(stk.pct/100)/days;
    const no=(rng()-0.49)*0.018;
    const cl=Math.max(p*0.5,p*(1+dr+no));
    const hi=cl*(1+rng()*0.020);
    const lo=cl*(1-rng()*0.020);
    const volM=0.4+rng()*1.4+Math.abs(dr+no)*10;
    bars.push({open:p,hi,lo,close:cl,vol:Math.round(av*volM),pct:(dr+no)*100});
    p=cl;
  }
  return bars;
}

function calcStoch(bars,kP=14){
  if(bars.length<kP)return 50;
  const sl=bars.slice(-kP);
  const lo=Math.min(...sl.map(b=>b.lo)),hi=Math.max(...sl.map(b=>b.hi));
  const c=bars[bars.length-1].close;
  return hi===lo?50:Math.round((c-lo)/(hi-lo)*100);
}

function calcSMA(bars,period){
  if(bars.length<period)return bars[bars.length-1]?.close||0;
  return bars.slice(-period).reduce((s,b)=>s+b.close,0)/period;
}

