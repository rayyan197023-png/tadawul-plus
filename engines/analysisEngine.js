/* ① حساب حجم المركز الأمثل */
function calcPositionSize(health, riskGateLevel){
  var score  = health.score  || 50;
  var grade  = health.grade  || "D";
  var conf   = health.confidence || 50;
  var sig    = health.sig    || "";
  var regime = health.regime || "chop";
  var layers = health.layers || {};

  // ══ الخطوة ١: Half-Kelly الحقيقي ══
  // p = احتمال الربح (من score + conf)
  // b = نسبة الربح/الخسارة (من R:R تاريخي، افتراضي 1.5)
  var p = Math.min(0.80, Math.max(0.20,
    (score/100)*0.55 + (conf/100)*0.30 + (sig==="شراء قوي"?0.10:sig==="مراقبة"?0.05:0)
  ));
  var b = sig==="شراء قوي"&&score>=75 ? 1.8   // R:R أعلى للإشارات القوية
        : sig==="شراء قوي"            ? 1.5
        : 1.2;

  var q        = 1 - p;
  var fullKelly = Math.max(0, (p*b - q) / b);  // Kelly كامل
  var halfKelly = fullKelly * 0.5;               // Half-Kelly — المعيار المؤسسي

  // ══ الخطوة ٢: تعديل التقلب (Volatility Scaling) ══
  // السهم المتقلب = حجم أصغر
  var L1 = layers.L1||50, L5 = layers.L5||50, L9 = layers.L9||50;
  var volScore = (100 - L5) / 100;  // L5 عالي = مؤشرات قوية = تقلب متحكم
  var volMulti = volScore>0.6?0.75:volScore>0.4?0.90:1.0;

  // ══ الخطوة ٣: تعديل الـ Regime ══
  var regimeMulti = regime==="volatile"   ? 0.55
                  : regime==="news-driven"? 0.65
                  : regime==="bear"       ? 0.70
                  : regime==="sideways"   ? 0.85
                  : regime==="bull"       ? 1.10
                  : 0.90; // chop

  // ══ الخطوة ٤: تعديل Risk Gate ══
  var gateMulti = riskGateLevel==="DANGER"  ? 0.0
               : riskGateLevel==="CAUTION"  ? 0.50
               : 1.0;

  // ══ الخطوة ٥: الحجم النهائي ══
  var rawSize = halfKelly * volMulti * regimeMulti * gateMulti;

  // حدود منطقية:
  // - لا تداول إذا الإشارة ضعيفة جداً
  // - سقف 30% لأي سهم واحد (حتى مع إشارة S ممتازة)
  // - حد أدنى 3% إذا قرر الدخول
  if(gateMulti===0||sig==="تخفيف"||(conf<45&&sig!=="شراء قوي")){
    rawSize = 0;
  } else if(rawSize>0 && rawSize<0.03){
    rawSize = 0.03;
  }

  var finalSize = Math.min(0.30, Math.max(0, rawSize));

  // ══ تسمية ذكية بناءً على الحجم الفعلي ══
  var label = finalSize>=0.22 ? "مركز كبير (22-30%)"
            : finalSize>=0.15 ? "مركز متوسط (15-21%)"
            : finalSize>=0.08 ? "مركز صغير (8-14%)"
            : finalSize>=0.03 ? "مركز رمزي (3-7%)"
            : "لا تداول";

  return {
    size:      +(finalSize).toFixed(3),
    pct:       Math.round(finalSize*100),
    label,
    gateLevel: riskGateLevel,
    // تفاصيل الحساب للشفافية
    kelly:     +(fullKelly*100).toFixed(1),
    halfKelly: +(halfKelly*100).toFixed(1),
    p:         +(p*100).toFixed(1),
    b:         +b.toFixed(1),
  };
}

/* ② فحص الارتباط — Markowitz */
function checkCorrelationGuard(sym, activePositions){
  // أيّ مجموعة ينتمي لها السهم؟
  var myGroup = null;
  Object.entries(TASI_CORR_GROUPS).forEach(function([g,syms]){
    if(syms.indexOf(sym)!==-1) myGroup=g;
  });
  if(!myGroup) return {safe:true, reason:""};

  // كم سهماً من نفس المجموعة في المحفظة؟
  var groupCount = (activePositions||[]).filter(function(p){
    return TASI_CORR_GROUPS[myGroup] && TASI_CORR_GROUPS[myGroup].indexOf(p.sym)!==-1;
  }).length;

  if(groupCount>=1){
    return {
      safe: false,
      reason: "⚠ المجموعة " + myGroup + " ممثّلة بالفعل — خطر تركّز",
      group: myGroup,
    };
  }
  return {safe:true, reason:"", group:myGroup};
}

/* ③ Dynamic Risk Gate */
function calcRiskGateLevel(){
  var vix     = MACRO.vix || 20;
  var regime  = MACRO.oilTasiRegime || "RALLY";
  var breadth = STOCKS.filter(function(x){return x.ch>0;}).length / STOCKS.length;

  // DANGER: VIX>30 أو CRASH Regime
  if(vix>30 || regime==="CRASH") return "DANGER";
  // CAUTION: VIX>20 أو breadth ضعيف
  if(vix>20 || breadth<0.35)     return "CAUTION";
  return "SAFE";
}

/* ④ Adaptive Signal Threshold */
function calcAdaptiveThreshold(allHealthData){
  if(!allHealthData||!allHealthData.length) return 45;

  var avgADX = allHealthData.reduce(function(s,h){
    return s+(h.layers&&h.layers.L1?h.layers.L1:50);
  },0)/allHealthData.length;

  var breadth = STOCKS.filter(function(x){return x.ch>0;}).length/STOCKS.length;
  var vix = MACRO.vix||20;

  // سوق صاعد قوي → عتبة أخفض (المزيد من الإشارات)
  if(avgADX>65 && breadth>0.60 && vix<20) return 38;
  // سوق جانبي → عتبة عادية
  if(avgADX<40 || (breadth>0.40 && breadth<0.60)) return 48;
  // سوق متقلب → عتبة عالية
  if(vix>25 || breadth<0.35) return 62;
  return 45; // default
}

/* ⑤ Sharpe Ratio Tracker */
function calcPortfolioSharpe(allHealthData){
  if(!allHealthData||allHealthData.length<2) return null;
  var scores = allHealthData.map(function(h){return h.score||50;});
  var avg    = scores.reduce(function(s,v){return s+v;},0)/scores.length;
  var std    = Math.sqrt(scores.reduce(function(s,v){return s+Math.pow(v-avg,2);},0)/scores.length);
  var rf     = (MACRO.saudiRepoRate||4.25)/252; // يومي
  var sharpe = std>0 ? +((avg/100*0.15 - rf) / (std/100)).toFixed(2) : 0;
  return {
    avg:+avg.toFixed(1), std:+std.toFixed(1), sharpe,
    label: sharpe>2?"ممتاز":sharpe>1?"جيد":sharpe>0.5?"مقبول":"ضعيف",
  };
}

function stockHealth(stk,bars){
  // ── A) المحرك التقني
  var tech   = calc9Layers(stk,bars);
  var LA     = tech.score;
  var regime = tech.regime;
  var layers = tech.layers;

  // ── B) المحرك الأساسي
  var fm  = calcFactorModel(stk,bars);
  var em  = calcEarningsModel(stk);
  var dcf = calcDCF(stk);
  var eq  = calcEarningsQuality(stk);
  var dcfScore = Math.round(_clamp(100/(1+Math.exp(-0.06*(dcf.upside-5))), 10, 95));
  var emScore  = Math.round(_clamp(100/(1+Math.exp(-0.05*(em.upside-8))),  10, 95));
  var fundConflict = (dcfScore>70&&eq.composite<40)?6:(dcfScore<40&&fm.composite>70)?4:0;
  var LB = _clamp(Math.round(dcfScore*0.35+fm.composite*0.30+emScore*0.20+eq.composite*0.15-fundConflict),0,100);

  // ── C) المحرك السلوكي المُعزَّز
  var opt = calcOptionsFlow(stk,bars);
  var ins = calcInsiderTransactions(stk,bars);
  var alt = calcAlternativeData(stk,bars);
  var optScore = _clamp(Math.round(80-(opt.putCallRatio-0.7)*60+(opt.unusualActivity&&opt.putCallRatio<0.9?10:0)),0,100);

  // ④ Earnings Surprise — هل الأرباح الفعلية تتجاوز التوقعات؟
  // eps_q1/q2/q3 متوفرة من STOCKS → نحسب الاتجاه
  var earningsSurprise = 50; // default محايد
  if(stk.eps_q1 && stk.eps_q2 && stk.eps_q3){
    var qVals=[stk.eps_q1,stk.eps_q2,stk.eps_q3];
    var qTrend=(qVals[2]-qVals[0])/Math.max(Math.abs(qVals[0]),0.01); // نمو ربعي
    var qConsist=qVals[2]>qVals[1]&&qVals[1]>qVals[0]?1:qVals[2]<qVals[1]&&qVals[1]<qVals[0]?-1:0;
    earningsSurprise=_clamp(Math.round(50+qTrend*100+qConsist*12),0,100);
  } else if(stk.epsGrw>0){
    earningsSurprise=_clamp(Math.round(50+stk.epsGrw*2.5),0,100);
  }

  // ⑤ Sector Rotation Score — أيّ القطاعات تتصاعد الآن؟
  // نقارن أداء قطاع السهم بمتوسط السوق مع تعديل Macro
  var sectorStocks=STOCKS.filter(function(x){return x.sec===stk.sec;});
  var sectorAvgCh=sectorStocks.reduce(function(s,x){return s+x.ch;},0)/sectorStocks.length;
  var mktAvgCh=STOCKS.reduce(function(s,x){return s+x.ch;},0)/STOCKS.length;
  var sectorRot=sectorAvgCh-mktAvgCh; // القطاع يتفوق على السوق؟
  // مع تعزيز من oilCorr في بيئة oil premium
  if(MACRO.oilWarPremium&&stk.oilCorr>0.5)sectorRot+=MACRO.oilPrice>MACRO.oilTarget?1.5:-1.5;
  var sectorRotScore=_clamp(Math.round(50+sectorRot*15),0,100);

  // دمج LC مع المصادر الجديدة (أكثر استقلالية)
  var LC = Math.round(
    optScore*0.30 + ins.score*0.30 + alt.composite*0.15 +
    earningsSurprise*0.15 + sectorRotScore*0.10
  );
  var lbLcConflict = (LB>72&&LC<33)?5:(LC>72&&LB<33)?4:0;

  // ── D) المضاعفات الخارجية
  var risk  = calcRiskAttribution(stk,bars);
  var inter = calcIntermarket(stk);
  var micro = calcMicrostructure(stk,bars);
  var riskMult = risk.sortino>2.0?1.07:risk.sortino>1.0?1.03:risk.sharpe>0.5?1.00:risk.sharpe>0?0.96:0.89;
  var finalMult = _clamp(riskMult*inter.multiplier*(micro?micro.multiplier:1.0),0.70,1.30);

  // ── E) الأوزان الديناميكية LA/LB/LC
  var wA,wB,wC;
  switch(regime){
    case "bull":        wA=0.32;wB=0.40;wC=0.28;break;
    case "bear":        wA=0.28;wB=0.38;wC=0.34;break;
    case "sideways":    wA=0.40;wB=0.35;wC=0.25;break;
    case "volatile":    wA=0.45;wB=0.30;wC=0.25;break;
    case "news-driven": wA=0.35;wB=0.28;wC=0.37;break;
    default:            wA=0.38;wB=0.32;wC=0.30;
  }
  var bayesAdj = tech.extras&&tech.extras.bayesMult?tech.extras.bayesMult:1.0;

  // ── TASI Context من tech.tasiCtx ──
  var tasiCtx = tech.tasiCtx || null;

  // ── F) Conviction الأساسي مع تعزيز عند الاتفاق الكامل ──
  var rawConviction = (LA*wA+LB*wB+LC*wC)*finalMult*bayesAdj;

  // تعزيز إضافي: إذا كان LA عالياً جداً والـ Ensemble متفق كلياً
  if(ensemble && ensemble.agreementBoost>=1.10 && LA>=75){
    var laBoost = 1.0 + (LA-75)/200;
    rawConviction = (LA*wA*laBoost + LB*wB + LC*wC)*finalMult*bayesAdj;
  }
  var conviction0 = _clamp(Math.round(rawConviction-lbLcConflict),0,100);

  // ══════════════════════════════════════════════
  //  ① ENSEMBLE VOTING
  // ══════════════════════════════════════════════
  var ensemble = ensembleVote(LA, LB, LC, regime, tech.gates?tech.gates.passed:0, layers);
  var conviction = _clamp(Math.round(conviction0 * ensemble.agreementBoost), 0, 100);

  // ── Soft Vote تعديل للحالات الحدية ──
  if(Math.abs(conviction-50) < 10 && ensemble.softBull !== undefined){
    conviction = _clamp(Math.round(conviction + ensemble.softBull*6), 0, 100);
  }
  // ── Tech Consensus تعزيز ──
  if(ensemble.techConsensus !== 0){
    conviction = _clamp(conviction + ensemble.techConsensus*3, 0, 100);
  }

  // ── TASI Context تعزيز — فريد لتاسي ──
  if(tasiCtx){
    // ① TASI Dominance: كبار الأسهم يتفقون مع الاتجاه
    if(tasiCtx.domDir !== 0){
      var tasiDomBoost = tasiCtx.domDir * (Math.abs(tasiCtx.domDir)===1 ? 5 : 2);
      conviction = _clamp(conviction + tasiDomBoost, 0, 100);
    }
    // ② Oil-TASI Regime: CRASH خطير، RALLY داعم
    if(tasiCtx.tasiRegime === "CRASH")   conviction = _clamp(Math.round(conviction*0.88), 0, 100);
    if(tasiCtx.tasiRegime === "RALLY")   conviction = _clamp(Math.round(conviction*1.04), 0, 100);
    if(tasiCtx.tasiRegime === "DIVERGE") conviction = _clamp(Math.round(conviction*0.94), 0, 100);
    // ③ Retail Euphoria: إذا الأفراد يبالغون في التفاؤل → خطر انعكاس
    if(tasiCtx.retailEuphoria && conviction > 65)
      conviction = _clamp(conviction - 6, 0, 100);
    // ④ Sunday Effect: الأحد أقل موثوقية
    if(tasiCtx.isSunday && Math.abs(conviction-50) < 15)
      conviction = _clamp(conviction + (conviction>50?-3:3), 0, 100);
  }

  // ══════════════════════════════════════════════
  //  ② FEEDBACK LOOP — الأوزان التكيّفية
  // ══════════════════════════════════════════════
  var WC_adapted = applyFeedbackToWeights(tech.weights||{}, stk.sym);
  if(WC_adapted !== tech.weights){
    var L = layers;
    var rawAdapted = Math.round(
      (L.L9||0)*WC_adapted.L9+(L.L1||0)*WC_adapted.L1+(L.L5||0)*WC_adapted.L5+
      (L.L4||0)*WC_adapted.L4+(L.L8||0)*WC_adapted.L8+(L.L7||0)*WC_adapted.L7+
      (L.L6||0)*WC_adapted.L6+(L.L2||0)*WC_adapted.L2+(L.L3||0)*WC_adapted.L3
    );
    var LA_adapted = _clamp(rawAdapted, 0, 100);
    conviction = _clamp(Math.round(
      conviction*0.70 +
      (LA_adapted*wA+LB*wB+LC*wC)*finalMult*bayesAdj*ensemble.agreementBoost*0.30
    ), 0, 100);
  }

  // ══════════════════════════════════════════════
  //  ③ CONFIDENCE THRESHOLD
  // ══════════════════════════════════════════════
  var conflictCnt = tech.extras&&tech.extras.conflictCount?tech.extras.conflictCount:0;
  var ct = calcConfidenceThreshold(
    conviction, layers, ensemble, conflictCnt,
    tech.gates?tech.gates.passed:0, regime
  );

  // ── G) الإشارة النهائية المُعدَّلة ──
  var merged = tech;
  merged.score = conviction;
  merged.grade = conviction>=85?"S":conviction>=75?"A":conviction>=65?"B":conviction>=55?"C":conviction>=45?"D":"F";

  var sig, sigC;
  if(ct.shouldAbstain){
    // تحقق إضافي: حتى لو ct.shouldAbstain، إذا كان Soft قوي جداً نُصدر إشارة
    var softAbs=ensemble.softBull||0;
    if(Math.abs(softAbs)>0.45&&ensemble.techConsensus!==0&&Math.sign(softAbs)===ensemble.techConsensus){
      // Soft قوي + Tech Consensus = إشارة موثوقة رغم الامتناع
      sig  = softAbs>0?"مراقبة":"تخفيف";
      sigC = softAbs>0?"#f59e0b":"#f04f5a";
    } else {
      sig="انتظر"; sigC="#6b7280";
    }
  } else if(tech.gates&&tech.gates.passed===3&&conviction>=75&&ensemble.bullCount>=2&&ct.isStrong){
    sig="شراء قوي"; sigC="#10c97e";
  } else if(tech.gates&&tech.gates.passed===3&&conviction>=75&&ensemble.ensembleSig==="هبوطي"&&ct.isStrong){
    sig="مراقبة"; sigC="#f59e0b";
  } else if(tech.gates&&tech.gates.passed>=2&&conviction>=60&&ct.isNormal){
    sig="مراقبة"; sigC="#f59e0b";
  } else if(conviction>=48&&tech.gates&&tech.gates.passed>=1&&!ct.shouldAbstain){
    // تعزيز: إذا كان Soft + TC يتفقان مع الإشارة → رفع لـ "مراقبة"
    var softCV=ensemble.softBull||0;
    if(conviction>=52&&Math.abs(softCV)>0.30&&ensemble.techConsensus!==0&&
       Math.sign(softCV)===Math.sign(conviction-50)){
      sig="مراقبة"; sigC="#f59e0b";
    } else {
      sig="محايد"; sigC="#06b6d4";
    }
  } else if(conviction<40&&ensemble.bearCount>=2){
    sig="تخفيف"; sigC="#f04f5a";
  } else if(conviction<45&&!ct.shouldAbstain){
    sig="تخفيف"; sigC="#f04f5a";
  } else {
    sig="محايد"; sigC="#06b6d4";
  }
  merged.sig  = sig;
  merged.sigC = sigC;

  // ── H) Probability مُعدَّلة ──
  var bullBoost = ensemble.bullCount===3?15:ensemble.bullCount===2?8:0;
  var bearBoost = ensemble.bearCount===3?15:ensemble.bearCount===2?8:0;
  var pBull   = _clamp(conviction+bullBoost-bearBoost,0,100);
  var pBear   = _clamp(100-conviction+bearBoost-bullBoost-(tech.gates&&tech.gates.all?10:0),0,100);
  var pNeutral= _clamp(100-pBull-pBear+10,0,100);
  merged.probability = _softmax3(pBull, pBear, pNeutral);

  // ── I) Portfolio Engine ──
  var riskGateLevel = calcRiskGateLevel();
  merged.positionSize     = calcPositionSize(merged, riskGateLevel);
  merged.correlationGuard = checkCorrelationGuard(stk.sym, []);
  merged.riskGate         = riskGateLevel;
  merged.confidence       = ct ? ct.confidence : 50;

  // ── J) metadata الكاملة ──
  merged.conviction = {
    LA, LB, LC, wA, wB, wC,
    riskMult:+riskMult.toFixed(2), finalMult:+finalMult.toFixed(3),
    dcfScore, fmScore:fm.composite, emScore,
    eqScore:eq.composite, eqGrade:eq.grade,
    optScore, insScore:ins.score, altScore:alt.composite,
    dcfUpside:dcf.upside, emUpside:em.upside,
    dcfRating:dcf.rating, fmGrade:fm.grade,
    fundConflict, lbLcConflict, conviction0,
    ensemble, confidenceThreshold:ct,
    feedbackApplied: WC_adapted!==tech.weights,
    risk:{sharpe:risk.sharpe,sortino:risk.sortino,alpha:risk.alpha,volatility:risk.volatility},
    inter:{multiplier:inter.multiplier,signal:inter.signal},
    micro:micro?{composite:micro.composite,ofi:micro.ofi}:null,
    regime,
    tasiCtx: tech.tasiCtx || null,
  };

  return merged;
}


/* ══════════════════════════════════════════════════════════════
   ① Ensemble Voting Engine
   ثلاثة نماذج مستقلة تصوّت — القرار = أغلبية مرجّحة
   النموذج التقني (LA) + الأساسي (LB) + السلوكي (LC)
   كل نموذج يُصدر: "شراء" / "محايد" / "بيع"
   الثقة = قوة الاتفاق بين النماذج
══════════════════════════════════════════════════════════════ */
function ensembleVote(LA, LB, LC, regime, gates, layers){
  // layers اختياري — يُستخدم للتأكيد المتقدم
  const L7 = layers ? (layers.L7||50) : 50;
  const L1 = layers ? (layers.L1||50) : 50;
  const L4 = layers ? (layers.L4||50) : 50;
  const L5 = layers ? (layers.L5||50) : 50;
  const L9 = layers ? (layers.L9||50) : 50;

  // ── عتبات مُعايَرة بدقة ──
  // التقني أكثر استجابةً (±10)، الأساسي أكثر تحفظاً (±13)
  function modelVote(score, threshold_buy, threshold_sell){
    if(score >= threshold_buy)  return {dir:1,  strength:(score-threshold_buy)/(100-threshold_buy)};
    if(score <= threshold_sell) return {dir:-1, strength:(threshold_sell-score)/threshold_sell};
    return {dir:0, strength:0};
  }
  const techVote  = modelVote(LA, 60, 40); // حساس — يُصدر إشارة أبكر
  const fundVote  = modelVote(LB, 63, 37); // متوسط
  const behavVote = modelVote(LC, 60, 40); // مرن

  // ── أوزان مُحسَّنة حسب الـ Regime (LA أثقل دائماً) ──
  let wT, wF, wB;
  switch(regime){
    case "bull":        wT=0.43; wF=0.34; wB=0.23; break;
    case "bear":        wT=0.40; wF=0.38; wB=0.22; break;
    case "sideways":    wT=0.32; wF=0.45; wB=0.23; break;
    case "volatile":    wT=0.52; wF=0.25; wB=0.23; break;
    case "news-driven": wT=0.32; wF=0.30; wB=0.38; break;
    default:            wT=0.43; wF=0.34; wB=0.23;
  }

  const weightedDir = techVote.dir*wT + fundVote.dir*wF + behavVote.dir*wB;
  const votes    = [techVote.dir, fundVote.dir, behavVote.dir];
  const bullCount = votes.filter(v=>v>0).length;
  const bearCount = votes.filter(v=>v<0).length;
  const neutCount = votes.filter(v=>v===0).length;

  // ── L7 Bayesian كمحكّم مستقل ──
  const l7Signal = L7 > 62 ? 1 : L7 < 38 ? -1 : 0;
  const l7Strong = L7 > 72 || L7 < 28;

  // ── Trend Score من L1+L4+L5 ──
  const trendScore = (L1 + L4 + L5) / 3;
  const trendDir   = trendScore > 60 ? 1 : trendScore < 40 ? -1 : 0;

  // ── Boost مُحسَّن مع L7 + Trend ──
  let agreementBoost;
  if(bullCount===3 && l7Signal===1 && trendDir===1)        agreementBoost = 1.15; // تأكيد رباعي
  else if(bearCount===3 && l7Signal===-1 && trendDir===-1) agreementBoost = 1.15;
  else if((bullCount===3||bearCount===3) && gates===3)      agreementBoost = 1.12;
  else if(bullCount===3||bearCount===3)                     agreementBoost = 1.10;
  else if(bullCount===2||bearCount===2)                     agreementBoost = 1.03;
  else                                                      agreementBoost = 0.91; // تعارض

  // ── L7 Veto: الـ Bayesian يعارض الـ Ensemble بقوة → تخفيض ──
  const l7Veto = l7Strong && ((l7Signal===-1 && bullCount===3) || (l7Signal===1 && bearCount===3));
  if(l7Veto) agreementBoost *= 0.82;

  // ── ① Soft Voting: قوة مستمرة لكل نموذج (بدل 0/1/-1 فقط) ──
  const softT = _clamp((LA-50)/50, -1, 1);
  const softF = _clamp((LB-50)/50, -1, 1);
  const softB = _clamp((LC-50)/50, -1, 1);
  const softBull = +(softT*wT + softF*wF + softB*wB).toFixed(3);

  // ── ② Tech Consensus: L1+L5+L9 كلهم في نفس الاتجاه = تأكيد قوي ──
  const techConsensus = L1>56&&L5>56&&L9>56 ? 1
                      : L1<44&&L5<44&&L9<44 ? -1 : 0;

  // ── ③ Gate Quality Score مستمر [0,1] ──
  const gq1 = _clamp((L9-48)/52, 0, 1);
  const gq2 = _clamp((L1-44)/56, 0, 1);
  const gq3 = _clamp(((L4+L5)/2-44)/56, 0, 1);
  const gateQuality = +((gq1+gq2+gq3)/3).toFixed(3);

  // ── عتبة الإشارة ──
  const signalThreshold = 0.16; // أكثر حساسية

  const agreement = bullCount===3||bearCount===3 ? "كامل"
                  : bullCount===2||bearCount===2 ? "جزئي"
                  : neutCount>=2                  ? "محايد"
                  : "متعارض";

  return {
    weightedDir:+weightedDir.toFixed(3),
    softBull, techConsensus, gateQuality,
    agreement, agreementBoost:+agreementBoost.toFixed(3),
    votes:{tech:techVote.dir, fund:fundVote.dir, behav:behavVote.dir},
    bullCount, bearCount, neutCount,
    l7Signal, l7Veto, trendDir, trendScore:+trendScore.toFixed(1),
    signalThreshold,
    ensembleSig: weightedDir > signalThreshold ? "صعودي"
               : weightedDir < -signalThreshold ? "هبوطي"
               : "محايد"
  };
}

/* ══════════════════════════════════════════════════════════════
   ② Confidence Threshold Engine
   "لا إشارة" أفضل من إشارة خاطئة
   يحسب درجة اليقين — إذا كانت منخفضة → يُصدر "انتظر"
   المعايير: تعارض الطبقات + قوة الأوزان + اتساق الإشارات
══════════════════════════════════════════════════════════════ */
function calcConfidenceThreshold(score, layers, ensemble, conflictCount, gates, regime){
  const {L1, L4, L5, L7, L9} = layers;

  // ① اتساق 4 طبقات أساسية (L1+L4+L5+L9)
  const coreScores  = [L1, L4, L5, L9];
  const coreMean    = coreScores.reduce((s,v)=>s+v,0)/4;
  const coreStd     = Math.sqrt(coreScores.reduce((s,v)=>s+Math.pow(v-coreMean,2),0)/4);
  const coreConsist = _clamp(1 - coreStd/48, 0, 1);

  // ② بايزي مُعزَّز
  const bayesConf  = _clamp(Math.abs(L7-50)/50, 0, 1);
  const bayesStrong = L7 > 70 || L7 < 30;

  // ③ اتفاق الـ Ensemble
  const ensembleConf = ensemble.bullCount===3||ensemble.bearCount===3 ? 1.0
                     : ensemble.bullCount===2||ensemble.bearCount===2 ? 0.78
                     : ensemble.neutCount>=2                           ? 0.42
                     : 0.18;

  // ④ Gate Quality مستمر (من ensembleVote)
  const gateQual  = ensemble.gateQuality || (gates===3?1.0:gates===2?0.70:gates===1?0.40:0.12);
  const gatesConf = gateQual;

  // ⑤ Trend Confirmation
  const trendScore = (L1+L4+L5)/3;
  const trendDist  = _clamp(Math.abs(trendScore-50)/50, 0, 1);

  // ⑥ عقوبة التعارض
  const conflictPenalty = _clamp(conflictCount * 0.26, 0, 0.65);

  // ⑦ بُعد الدرجة عن المركز
  const scoreDist = _clamp(Math.abs(score-50)/50, 0, 1);

  // ⑧ Soft Bull من Ensemble يُعزز الثقة عند الحالات الحدية
  const softConf = _clamp(Math.abs(ensemble.softBull||0) * 0.12, 0, 0.12);

  // ⑨ Tech Consensus يُضيف ثقة إضافية
  const tcConf = ensemble.techConsensus!==0 ? 0.04 : 0;

  const rawConf = (
    coreConsist  * 0.19 +
    bayesConf    * 0.21 +
    ensembleConf * 0.30 +
    gatesConf    * 0.22 +
    trendDist    * 0.04 +
    scoreDist    * 0.04
  ) * (1 - conflictPenalty);

  const bayesBoost = bayesStrong&&(ensemble.bullCount>=2||ensemble.bearCount>=2)?0.05:0;
  const confidence = _clamp(Math.round((rawConf+bayesBoost+softConf+tcConf)*100), 0, 100);

  // Adaptive ABSTAIN — عتبة ديناميكية حسب حالة السوق
  // VIX<18: هادئ → عتبة أخفض (مزيد إشارات)
  // VIX>25: متقلب → عتبة أعلى (إشارات أقل وأقوى)
  // VIX>32: خطر → عتبة عالية جداً
  const vixNow = MACRO.vix || 20;
  const ABSTAIN_LEVEL = vixNow < 18 ? 40
                      : vixNow < 25 ? 45
                      : vixNow < 32 ? 58
                      : 70;
  const THRESHOLD_NORMAL = 55;
  const THRESHOLD_STRONG = 68;

  // Anti-False-Signal مُحسَّن: WD ضعيف أيضاً سبب للامتناع
  const weakSignal    = Math.abs(score-50) < 7;
  const wdWeak        = Math.abs(ensemble.weightedDir||0) < 0.15;
  const shouldAbstain = confidence < ABSTAIN_LEVEL || (weakSignal && (conflictCount>=1||wdWeak));
  const isStrong      = confidence >= THRESHOLD_STRONG && !shouldAbstain;
  const isNormal      = confidence >= THRESHOLD_NORMAL && !isStrong && !shouldAbstain;

  return {
    confidence,
    shouldAbstain,
    isStrong,
    isNormal,
    coreConsist:+coreConsist.toFixed(3),
    bayesConf:+bayesConf.toFixed(3),
    ensembleConf:+ensembleConf.toFixed(3),
    gatesConf:+gatesConf.toFixed(3),
    label: confidence>=75?"ثقة عالية"
         : confidence>=55?"ثقة معقولة"
         : confidence>=35?"ثقة منخفضة"
         : "ثقة ضعيفة — انتظر"
  };
}

/* ══════════════════════════════════════════════════════════════
   ③ Feedback Loop — Adaptive Weight Calibration
   يتتبع الإشارات السابقة ويُعدّل الأوزان تلقائياً
   مبدأ: الطبقات التي أثبتت دقتها تحصل على وزن أعلى
   التخزين: sessionStorage (مؤقت، يُصفَّر بكل جلسة)
══════════════════════════════════════════════════════════════ */
const FEEDBACK_STORE_KEY = 'tdw_feedback_v2';

function loadFeedbackState(){
  try{
    const raw = typeof sessionStorage !== 'undefined'
      ? sessionStorage.getItem(FEEDBACK_STORE_KEY) : null;
    if(!raw) return null;
    return JSON.parse(raw);
  }catch(e){return null;}
}

function saveFeedbackState(state){
  try{
    if(typeof sessionStorage !== 'undefined')
      sessionStorage.setItem(FEEDBACK_STORE_KEY, JSON.stringify(state));
  }catch(e){}
}

function getAdaptiveWeightAdjustment(sym){
  /* يُرجع delta للأوزان بناءً على سجل أداء هذا السهم:
     إذا كانت إشارات L9 صحيحة باستمرار → رفع وزنها
     إذا كانت L5 تُخطئ كثيراً → خفض وزنها        */
  const state = loadFeedbackState();
  if(!state || !state[sym]) return null;
  const perf = state[sym];
  if(perf.total < 3) return null; // نحتاج 3 إشارات مُتحقَّق منها على الأقل

  const adj = {};
  // لكل طبقة: إذا كانت دقتها > 70% رفع وزنها، < 40% خفضه
  Object.keys(perf.layers||{}).forEach(k=>{
    const lp = perf.layers[k];
    if(!lp || lp.total < 2) return;
    const acc = lp.correct / lp.total;
    if     (acc > 0.70) adj[k] = +0.02;  // دقيقة → رفع 2%
    else if(acc > 0.55) adj[k] = +0.01;  // مقبولة → رفع 1%
    else if(acc < 0.40) adj[k] = -0.015; // ضعيفة → خفض 1.5%
    else if(acc < 0.30) adj[k] = -0.025; // سيئة → خفض 2.5%
  });

  return Object.keys(adj).length > 0 ? adj : null;
}

function recordFeedback(sym, signal, layers, actualOutcome){
  /* actualOutcome: +1 صح، -1 خطأ
     يُسجَّل من خارج الدالة عند توفر نتيجة فعلية */
  const state = loadFeedbackState() || {};
  if(!state[sym]) state[sym] = {total:0, correct:0, layers:{}};
  const perf = state[sym];
  perf.total++;
  if(actualOutcome > 0) perf.correct++;

  // تتبع دقة كل طبقة
  const lnames = ['L1','L2','L3','L4','L5','L6','L7','L8','L9'];
  lnames.forEach(k=>{
    if(layers[k] === undefined) return;
    if(!perf.layers[k]) perf.layers[k] = {total:0, correct:0};
    perf.layers[k].total++;
    // الطبقة "صح" إذا كانت تشير نفس الاتجاه للنتيجة الفعلية
    const layerDir = layers[k] > 55 ? 1 : layers[k] < 45 ? -1 : 0;
    const signalDir = signal==='شراء قوي'||signal==='مراقبة' ? 1
                    : signal==='تخفيف' ? -1 : 0;
    if(layerDir !== 0 && layerDir === signalDir && actualOutcome > 0)
      perf.layers[k].correct++;
    else if(layerDir !== 0 && layerDir !== signalDir && actualOutcome < 0)
      perf.layers[k].correct++;
  });

  // احتفظ بآخر 100 سجل فقط
  if(perf.total > 100){ perf.total=50; perf.correct=Math.round(perf.correct*0.5); }
  saveFeedbackState(state);
}

/* ══ applyFeedbackToWeights: تطبيق الضبط التكيّفي على الأوزان ══ */
function applyFeedbackToWeights(WC, sym){
  const adj = getAdaptiveWeightAdjustment(sym);
  if(!adj) return WC; // لا تاريخ كافٍ → الأوزان كما هي

  const result = {...WC};
  const keys = Object.keys(result);

  // تطبيق التعديلات
  keys.forEach(k=>{ if(adj[k]) result[k] = Math.max(0.005, result[k] + adj[k]); });

  // إعادة تطبيع Σ=1
  const total = keys.reduce((s,k)=>s+result[k],0);
  keys.forEach(k=>{ result[k] = +(result[k]/total).toFixed(4); });

  return result;
}

