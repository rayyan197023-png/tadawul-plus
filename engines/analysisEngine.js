/* ── مساعد: clamp + sigmoid + softmax ── */
function _clamp(v,lo,hi){return Math.min(hi,Math.max(lo,v));}
function _sig(x){return 1/(1+Math.exp(-x));}
function _softmax3(a,b,c){
  const ea=Math.exp(_clamp(a/30,-4,4)),eb=Math.exp(_clamp(b/30,-4,4)),ec=Math.exp(_clamp(c/30,-4,4));
  const s=ea+eb+ec;
  return{bull:Math.round(ea/s*100),bear:Math.round(eb/s*100),neutral:Math.round(ec/s*100)};
}

/* ── 1) detectMarketRegime الموسّع ── */
function detectMarketRegime(bars, adxV, mktWtd, mktBreadth, atr, stk){
  const n = bars.length||1;

  // تقلبية: نسبة ATR إلى السعر
  const atrPct = atr / (bars[n-1].c||1) * 100;

  // زخم السوق: متوسط تغير آخر 5 أيام
  const ret5 = bars.length>=5 ? bars.slice(-5).reduce((s,b)=>s+b.pct,0)/5 : 0;

  // ATR متغير: هل تقلّب اليوم أعلى من المعدل؟
  const recentAtrPct = bars.length>=5
    ? bars.slice(-5).reduce((s,b)=>s+Math.abs(b.pct),0)/5
    : Math.abs(stk.ch);
  const historicAtrPct = bars.length>=20
    ? bars.slice(-20).reduce((s,b)=>s+Math.abs(b.pct),0)/20
    : recentAtrPct;
  const volSpike = historicAtrPct>0 ? recentAtrPct/historicAtrPct : 1;

  // السوق الجانبي: ADX ضعيف + مدى ضيق
  const isSideways = adxV < 20 && atrPct < 1.2;

  // سوق متقلب: ارتفاع مفاجئ في التقلبية
  const isVolatile  = volSpike > 1.8 || atrPct > 2.5;

  // سوق مدفوع بأخبار: VIX عالٍ + حجم غير عادي
  const avgVol = bars.reduce((s,b)=>s+b.vol,0)/n;
  const vol5   = bars.length>=5 ? bars.slice(-5).reduce((s,b)=>s+b.vol,0)/5 : avgVol;
  const volRatio = avgVol>0 ? vol5/avgVol : 1;
  const isNewsdriven = MACRO.vix > 28 && volRatio > 1.6;

  // سوق صاعد/هابط
  const isTrending = adxV > 28 && Math.abs(mktWtd) > 0.25;
  const isBullish  = isTrending && mktWtd > 0 && mktBreadth > 0.55;
  const isBearish  = isTrending && mktWtd < 0 && mktBreadth < 0.45;

  // الأولوية: volatile > news-driven > sideways > bull/bear > chop
  let regime;
  if      (isVolatile)   regime = "volatile";
  else if (isNewsdriven) regime = "news-driven";
  else if (isSideways)   regime = "sideways";
  else if (isBullish)    regime = "bull";
  else if (isBearish)    regime = "bear";
  else                   regime = "chop";

  return {regime, atrPct:+atrPct.toFixed(2), volSpike:+volSpike.toFixed(2), ret5:+ret5.toFixed(3), volRatio:+volRatio.toFixed(2)};
}

/* ── 2) Dynamic Weights حسب الـ Regime + القطاع ── */
function buildDynamicWeights(regime, sector){
  /* الأوزان الأساسية (تعكس المجموعات الجديدة):
     Momentum(L5):20% Trend(L1):20% Liquidity(L9):20%
     Structure(L4):15% Macro(L8):15% Extras(L6,L7,L2,L3):10%  → Σ=1 */
  const BASE = {
    L9:0.20, L1:0.20, L5:0.20,
    L4:0.15, L8:0.15,
    L7:0.06, L6:0.04, L2:0.02, L3:0.02
  }; // Σ = 1.04 → سنُعاد التطبيع

  // دلتا حسب الـ Regime — معايرة دقيقة
  // المبدأ: كل regime يُبرز المحركات الأنسب لطبيعته
  // bull:        زخم + هيكل يقودان
  // bear:        سيولة + بايزي (دفاعي)
  // sideways:    بايزي + ماكرو (انتظار)
  // volatile:    سيولة أقوى (L9+.06 بدل +.04) — الحجم هو الحَكَم في التقلب
  // news-driven: ماكرو أولاً + سيولة ثانياً
  // chop:        بايزي + ماكرو (لا ثقة في الاتجاه)
  const DELTA = {
    bull:        {L5:+.05, L1:+.03, L4:+.02, L9:+.01, L8:-.02, L7:-.03, L6:-.02, L2:-.02, L3:-.02},
    bear:        {L9:+.06, L7:+.03, L8:+.02, L1:+.01, L5:-.05, L4:-.03, L6:-.02, L2:-.01, L3:-.01},
    sideways:    {L7:+.05, L8:+.04, L9:+.02, L1:-.03, L5:-.05, L4:-.02, L6:+.01, L2:-.01, L3:-.01},
    volatile:    {L9:+.06, L8:+.04, L7:+.02, L5:-.05, L1:-.04, L4:-.02, L6:-.01, L2:0,    L3:0   },
    "news-driven":{L8:+.06, L9:+.03, L7:+.02, L5:-.04, L1:-.03, L4:-.02, L6:-.01, L2:-.01, L3:0  },
    chop:        {L7:+.04, L8:+.03, L9:+.02, L5:-.03, L1:-.03, L4:-.02, L6:0,    L2:-.01, L3:0   },
  };

  // دلتا حسب القطاع
  const SECTOR_D = {
    "طاقة" :  {L9:+.04, L8:+.03, L1:+.01, L5:-.03, L4:-.02, L7:-.02, L6:-.01},
    "بترو" :  {L9:+.03, L8:+.03, L1:+.01, L5:-.02, L4:-.02, L7:-.02, L6:-.01},
    "بنوك" :  {L8:+.03, L7:+.03, L5:+.01, L9:-.02, L1:-.02, L4:-.01, L6:-.01, L2:-.01},
    "تقنية":  {L5:+.03, L1:+.03, L4:+.01, L9:-.02, L8:-.02, L7:-.01, L6:-.01, L2:-.01},
    "تعدين":  {L9:+.03, L1:+.03, L4:+.01, L8:-.02, L5:-.02, L7:-.01, L6:-.01, L2:-.01},
    "غذاء" :  {L8:+.03, L7:+.02, L9:+.01, L5:-.02, L1:-.02, L4:-.01, L6:-.01},
    "تأمين":  {L8:+.03, L7:+.02, L9:+.01, L5:-.02, L1:-.02, L4:-.01, L6:-.01},
  };

  const W = {...BASE};
  const rd = DELTA[regime] || DELTA.chop;
  const sd = SECTOR_D[sector] || {};

  // تطبيق الدلتا بوزن: Regime 70% + Sector 30% (معايرة من الصور)
  ['L1','L2','L3','L4','L5','L6','L7','L8','L9'].forEach(k=>{
    W[k] = (BASE[k]||0) + (rd[k]||0)*0.70 + (sd[k]||0)*0.30;
    W[k] = Math.max(0.01, W[k]); // لا وزن سالب
  });

  // إعادة التطبيع → Σ = 1
  const total = Object.values(W).reduce((s,v)=>s+v,0);
  ['L1','L2','L3','L4','L5','L6','L7','L8','L9'].forEach(k=>{
    W[k] = +(W[k]/total).toFixed(4);
  });

  return W;
}

/* ── 3) Correlation Control ── */
function reduceCorrelation(layers){
  /* أزواج مترابطة معروفة:
     L1(هيكل) ↔ L4(RSC/VWAP): كلاهما يعتمد على اتجاه السعر
     L5(RSI/MACD) ↔ L2(Effort/OBV): كلاهما حجم + زخم
     L9(CMF/OBV) ↔ L2(Effort): قياس حجم مشترك
  */
  const {L1,L2,L4,L5,L9} = layers;
  let W_corr = {L1:1, L2:1, L3:1, L4:1, L5:1, L6:1, L7:1, L8:1, L9:1};

  // ── أزواج مترابطة معروفة ──

  // L1↔L4 (هيكل↔قوة نسبية): كلاهما يعتمد على اتجاه السعر — corr≈0.72
  if(Math.abs(L1-L4) < 15) W_corr.L4 = 0.75; // كان 0.80 → أشد قليلاً

  // L5↔L2 (مؤشرات↔جهد/OBV): حجم + زخم — corr≈0.68
  if(Math.abs(L5-L2) < 15) W_corr.L2 = 0.70; // كان 0.75

  // L9↔L2 (سيولة↔جهد): قياس حجم مشترك — corr≈0.65
  if((L9>65&&L2>65)||(L9<40&&L2<40)) W_corr.L2 = Math.min(W_corr.L2, 0.65); // كان 0.70

  // L1↔L5 (هيكل↔مؤشرات): corr≈0.55 — جديد
  // عند اتفاقهما بقوة، قلّل L5 قليلاً (L1 أشمل)
  if(Math.abs(L1-L5) < 12) W_corr.L5 = 0.90;

  // L7↔L9 (بايزي↔سيولة): L7 يستخدم L9 كمدخل → تكرار جزئي — corr≈0.45
  // خفض طفيف فقط لأن L7 يضيف Bayesian update فوق L9
  if(layers.L7 !== undefined){
    const _L7raw = layers.L7;
    if((L7>65&&L9>65)||(L7<40&&L9<40)) W_corr.L7 = 0.92;
  }

  return W_corr;
}

/* ── 4) Macro Impact Gate ── */
function applyMacroGate(rawScore, macroScore100){
  // macroScore100 هو mc.score * 5 (تحويل من [0,20] → [0,100])
  let multiplier = 1.0;
  if      (macroScore100 < 25) multiplier = 0.82; // بيئة حرجة جداً → خفض 18%
  else if (macroScore100 < 40) multiplier = 0.91; // بيئة سلبية → خفض 9%
  else if (macroScore100 > 75) multiplier = 1.06; // بيئة إيجابية → رفع 6%
  else if (macroScore100 > 60) multiplier = 1.03; // بيئة جيدة → رفع 3%
  return _clamp(rawScore * multiplier, 0, 100);
}

/* ── 5) Progressive Conflict Penalty ── */
function calcConflictPenalty(layers, regime){
  const {L1,L4,L5,L7,L9} = layers;

  // تعريف التعارضات مع شدة كل منها
  const conflicts = [
    // هيكل صاعد بقوة ≠ مؤشرات هابطة → تعارض خطير
    {active: L1>75 && L5<30,  severity: 3, label:"هيكل↑ مؤشرات↓"},
    // سيولة عالية ≠ هيكل هابط
    {active: L9>75 && L1<30,  severity: 3, label:"سيولة↑ هيكل↓"},
    // زخم قوي ≠ سيولة ضعيفة
    {active: L5>75 && L9<30,  severity: 2, label:"زخم↑ سيولة↓"},
    // بايزي متفائل ≠ سيولة هابطة
    {active: L7>75 && L9<35,  severity: 2, label:"بايزي↑ سيولة↓"},
    // قوة نسبية ≠ هيكل هابط
    {active: L4>70 && L1<30,  severity: 1, label:"قوة↑ هيكل↓"},
  ];

  const active = conflicts.filter(c=>c.active);
  if(!active.length) return {penalty:0, conflictCount:0, details:[]};

  // العقوبة التدريجية: الأولى أقل، كل تعارض إضافي يُضاعف
  let penalty = 0;
  active.forEach((c,i)=>{
    // العقوبة الأساسية حسب الشدة × معامل تصاعدي
    penalty += c.severity * 3 * (1 + i * 0.5);
  });

  // في السوق المتقلب عقوبة أعلى
  if(regime === "volatile") penalty *= 1.3;
  if(regime === "news-driven") penalty *= 1.2;

  return {
    penalty: Math.round(_clamp(penalty, 0, 25)),
    conflictCount: active.length,
    details: active.map(c=>c.label)
  };
}


